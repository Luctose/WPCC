# Determines if there is a collision on the grid
def collide(falling, ground):
    # Determine by comparing each spot side by side
    for item in range(len(falling)):
        for num in range(len(falling[item])):
            if falling[item][num] == 1 and ground[item][num] == 1:
                # If there is collision
                return True

    # If no collision occurs
    return False


